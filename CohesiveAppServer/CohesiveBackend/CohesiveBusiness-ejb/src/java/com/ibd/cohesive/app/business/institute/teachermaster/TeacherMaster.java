package com.ibd.cohesive.app.business.institute.teachermaster;

/**
 *
 * @author DELL
 */
public class TeacherMaster {
    String teacherID;
    String teacherName;
    String teacherShortName;

    public String getTeacherID() {
        return teacherID;
    }

    public void setTeacherID(String teacherID) {
        this.teacherID = teacherID;
    }

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }


    public String getTeacherShortName() {
        return teacherShortName;
    }

    public void setTeacherShortName(String teacherShortName) {
        this.teacherShortName = teacherShortName;
    }
    
}
