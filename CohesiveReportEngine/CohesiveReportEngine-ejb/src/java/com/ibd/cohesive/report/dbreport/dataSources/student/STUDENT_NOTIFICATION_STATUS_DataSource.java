package com.ibd.cohesive.report.dbreport.dataSources.student;

import com.ibd.businessViews.IStudentDataSet;
import com.ibd.cohesive.report.dbreport.dataModels.student.STUDENT_NOTIFICATION_STATUS;
import com.ibd.cohesive.report.dependencyinjection.ReportDependencyInjection;
import com.ibd.cohesive.report.preprocessor.IPreProcessor;
import java.util.ArrayList;
import java.util.List;
import javax.naming.NamingException;

/**
 *
 * @author ibdtech
 */
public class STUDENT_NOTIFICATION_STATUS_DataSource extends StudentDataSource<STUDENT_NOTIFICATION_STATUS>{
 
    String fileName;
    //String tableName;
    String instanceID;
    
    @Override
      public List<STUDENT_NOTIFICATION_STATUS> fetch()
	{
	
            try
            {
                //parseParameters();
                fileName = this.getStudentId();
                instanceID =this.getInstituteID();
                
                ArrayList<STUDENT_NOTIFICATION_STATUS> resultset=new ArrayList();
                ReportDependencyInjection inject=new ReportDependencyInjection();
                
                IPreProcessor preProcessor=inject.getPreProcessor();
                
                if( preProcessor.preProcessing(this.getNokotser(), this.getUserID(), this.getLoginInstitute(), this.getService())){
                   IStudentDataSet appDataSet=preProcessor.getStudentDataset();
                   
                  String result= appDataSet.getSTUDENT_NOTIFICATION_STATUS_DataSet(fileName, instanceID);
                  
                  STUDENT_NOTIFICATION_STATUS errorMaster=new STUDENT_NOTIFICATION_STATUS();
                 resultset= errorMaster.convertStringToArrayList(result);
        
                
                   return resultset;
                }else{
                    return null;
                } 
        
            
            
       } catch(NamingException ex){
           return null;
//             throw new EJBException();
       } catch(Exception ex){
           return null;
              
    }
        }     
       
    
}
